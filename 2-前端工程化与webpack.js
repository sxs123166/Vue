// 1. 什么是前端工程化

// 实际的前端开发：
// 模块化（js的模块化、css的模块化、资源的模块化）
// 组件化（复用现有的UI结构、样式、行为）
// 规范化（目录结构的划分、编码规范化、接口规范化、文档规范化、Git分支管理）
// 自动化（自动化构建、自动部署、自动测试）

// 前端工程化指的是：在企业级的开发项目中，把前端开发所需的工具、技术、流程、经验等进行规范化、标准化

// 企业中的Vue和React项目，都是基于工程化的方式进行开发的

